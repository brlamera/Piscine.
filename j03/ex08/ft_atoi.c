int		ft_atoi(char *str)
{
	int		nb;
	int		i;
	int		sg;

	nb = 0;
	i = 0;
	sg = 1;
	while (str[i] == ' ' || str[i] == '	')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '+')
			sg *= -1;
		sg *= -1;
		i++;
	}
	while (str[i] != '\0')
	{
		if (str[i] > '9' || str[i] < '0')
			return (nb * sg);
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * sg);
}
