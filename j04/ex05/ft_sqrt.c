int		ft_sqrt(int nb)
{
	int	i;
	int	sqrt;

	i = 1;
	sqrt = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i <= nb / 2)
	{
		sqrt = i * i;
		if (sqrt == nb)
		{
			return (i);
		}
		i = i + 1;
	}
	return (0);
}
