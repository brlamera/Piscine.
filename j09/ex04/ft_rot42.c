char	*ft_rot42(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = (((str[i] - 65) + 47) % 26) + 60;
		else if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = (((str[i] - 97) + 47) % 26) + 92;
		i++;
	}
	return (str);
}
